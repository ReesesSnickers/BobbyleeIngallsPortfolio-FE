export function createSlug(title: string): string {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

export function getProjectBySlug(slug: string) {
  const project = projectsData.find(
    (project) => createSlug(project.title) === slug
  );
  if (!project) {
    console.log(`Project not found for slug: ${slug}`);
    console.log(
      `Available slugs: ${projectsData
        .map((p) => createSlug(p.title))
        .join(", ")}`
    );
  }
  return project;
}

export function getAllProjectSlugs() {
  return projectsData.map((project) => createSlug(project.title));
}

